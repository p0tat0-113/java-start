package operator;

public class Operator5 {
    public static void main(String[] args){
        //대입연산자
        int a = 10;
        System.out.println(a += 2);
        System.out.println(a -= 2);
        System.out.println(a /= 2);
        System.out.println(a *= 2);
        System.out.println(a %= 2);
    }
}
